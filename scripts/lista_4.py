# Definición de una lista de números (numbers)
numbers = [17, 123, 87, 34, 66, 8398, 44]

# Imprimir el tercer elemento de la lista (índice 2)
print(numbers[2])

# Imprimir el primer elemento de la lista utilizando la expresión 9 - 8
print(numbers[9 - 8])

# Imprimir el segundo elemento desde el final de la lista (índice -2)
print(numbers[-2])

# Imprimir el último elemento de la lista utilizando la longitud de la lista
print(numbers[len(numbers) - 1])
