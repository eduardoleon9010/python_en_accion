"""
Este ejercicio realiza varias operaciones matemáticas utilizando distintos operadores:

Suma: Se suma el valor de valor_a (50) con valor_b (2), y se imprime el resultado.
Resta: Se resta valor_b (2) a valor_a (50), y se imprime el resultado.
Multiplicación: Se multiplica valor_a (50) por valor_b (2), y se imprime el resultado.
División: Se divide valor_a (50) entre valor_b (2), y se imprime el resultado.
Potenciación: Se eleva valor_a (50) a la potencia valor_b (2), y se imprime el resultado.
División entera: Se divide valor_a (6) entre valor_b (2) y se toma el cociente sin parte 
decimal, se imprime el resultado.
Módulo (residuo de la división): Se calcula el residuo de la división entre valor_a (40) 
y valor_b (3), y se imprime el resultado.
"""

#suma

valor_a = 50
valor_b = 2

resultado = valor_a + valor_b

print(resultado)

#Resta

valor_a = 50
valor_b = 2

resultado = valor_a - valor_b

print(resultado)

#Multiplicacion

valor_a = 50
valor_b = 2

resultado = valor_a * valor_b

print(resultado)

#Division

valor_a = 50
valor_b = 2

resultado = valor_a / valor_b

print(resultado)

#Potenciacion

valor_a = 50
valor_b = 2

resultado = valor_a ** valor_b

print(resultado)


#Division entero
valor_a = 6
valor_b = 2

resultado = valor_a // valor_b

print(resultado)

#Modulo(residuo de la division)
valor_a = 40
valor_b = 3

resultado = valor_a % valor_b

print(resultado)
