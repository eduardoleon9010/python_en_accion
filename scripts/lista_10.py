# Definición de una lista (alist)
alist = ['a', 'd', 'f']

# Insertar los elementos 'b' y 'c' en la posición 1 (sin reemplazar ningún elemento)
alist[1:1] = ['b', 'c']

# Imprimir la lista después de la primera modificación
print(alist)

# Insertar el elemento 'e' en la posición 4 (sin reemplazar ningún elemento)
alist[4:4] = ['e']

# Imprimir la lista después de la segunda modificación
print(alist)
