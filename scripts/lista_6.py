# Definición de una lista de frutas (fruit)
fruit = ["manzana", "naranja", "plátano", "cereza"]

# Concatenación de dos listas de números usando el operador +
print([1, 2] + [3, 4])

# Concatenación de la lista de frutas con otra lista de números
print(fruit + [6, 7, 8, 9])

# Creación de una lista de ceros repetidos cuatro veces
print([0] * 4)

# Repetición de la lista [1, 2, ["hola", "adiós"]] dos veces
print([1, 2, ["hola", "adiós"]] * 2)
