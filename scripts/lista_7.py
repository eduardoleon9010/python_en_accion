# Definición de una lista (a_list)
a_list = ['a', 'b', 'c', 'd', 'e', 'f']

# Obtener una porción de la lista desde el índice 1 hasta el índice 2 (sin incluir el índice 3)
print(a_list[1:3])

# Obtener una porción de la lista desde el principio hasta el índice 3 (sin incluir el índice 4)
print(a_list[:4])

# Obtener una porción de la lista desde el índice 3 hasta el final
print(a_list[3:])

# Obtener una copia completa de la lista
print(a_list[:])
