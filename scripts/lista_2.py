# Definición de una lista mixta (mixedlist) que contiene una cadena, un número decimal, una expresión aritmética y otra lista.
mixedlist = ["hola", 2.0, 5 * 2, [10, 20]]

# Imprimir la lista de números (numbers)
print(numbers)

# Imprimir la lista mixta (mixedlist)
print(mixedlist)

# Crear una nueva lista (newlist) que contiene las listas de números (numbers) y palabras clave (vocabulary)
newlist = [numbers, vocabulary]

